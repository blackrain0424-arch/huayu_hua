DailyFlower getTodayFlower() {
  final now = DateTime.now();
  final dayOfYear = int.parse(
    '${now.year}${now.month.toString().padLeft(2, '0')}${now.day.toString().padLeft(2, '0')}',
  );
  // Use the day of year to deterministically pick a flower
  final index = dayOfYear % _flowerCalendar.length;
  return _flowerCalendar[index];
}
